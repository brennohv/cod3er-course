const a = 7 // = é um sinal de atribuição, nao quer dizer que a é igual a 7, e sim que A recebe valor de 7.
let b = 3

b += a /* b = b + a          Nesse caso quando colocamos um + antes do = ele faz uma operação aritimetica e soma os valores,
o resultado sera o novo valor da variavel atribuida */  
console.log(b)

b -= 4 // b = b - 4
console.log(b)

b *= 2 // b = b * 2
console.log(b)

b /= 2 // b = b / 2
console.log(b)

b %= 2 // b = b % 2
console.log(b)
